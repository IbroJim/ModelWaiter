package com.company.temel;

public class Waiter {

    private int id;
    private String name;

    public Waiter( String name){
        this.id=id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
