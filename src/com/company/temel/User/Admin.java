package com.company.temel.User;

public class Admin {

    private int id;
    private String name;

    public Admin( String name){
        this.id=id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}


